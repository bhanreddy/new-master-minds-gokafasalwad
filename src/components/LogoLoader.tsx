const PATHS = [P0, P1, P2];

// ─── Component ────────────────────────────────────────────────────────────────
export default function LogoLoader({ size = 30, color = '#111111', style, isReady, onLogoRevealed }: LogoLoaderProps) {
  const drawAnims = useRef(PATHS.map(() => new Animated.Value(1))).current;
  const masterOpacity = useRef(new Animated.Value(1)).current;
  const fillAnim = useRef(new Animated.Value(0)).current;

  const isReadyRef = useRef(isReady);
  isReadyRef.current = isReady;

  const onLogoRevealedRef = useRef(onLogoRevealed);
  onLogoRevealedRef.current = onLogoRevealed;

  useEffect(() => {
    let cancelled = false;
    let poller: ReturnType<typeof setInterval>;

    const playAnimation = () => {
      if (cancelled) return;

      drawAnims.forEach(a => a.setValue(1));
      fillAnim.setValue(0);
      masterOpacity.setValue(1);

      const drawSteps = drawAnims.map((anim, i) =>
        Animated.timing(anim, {
          toValue: 0,
          duration: DRAW_DURATIONS[i],
          easing: Easing.inOut(Easing.cubic),
          useNativeDriver: false,
        })
      );

      Animated.sequence(drawSteps).start(() => {
        if (cancelled) return;

        const proceedWithFill = () => {
          Animated.sequence([
            Animated.delay(150),
            Animated.timing(fillAnim, {
              toValue: 1,
              duration: FILL_DURATION,
              easing: Easing.out(Easing.quad),
              useNativeDriver: false,
            }),
            Animated.delay(HOLD_DURATION),
          ]).start(() => {
            if (cancelled) return;

            if (onLogoRevealedRef.current) {
              onLogoRevealedRef.current();
            }

            if (isReadyRef.current === undefined) {
              Animated.timing(masterOpacity, {
                toValue: 0,
                duration: FADE_DURATION,
                useNativeDriver: false,
              }).start(() => {
                if (!cancelled) playAnimation();
              });
            }
          });
        };

        if (isReadyRef.current === undefined || isReadyRef.current === true) {
          proceedWithFill();
        } else {
          poller = setInterval(() => {
            if (cancelled) {
              clearInterval(poller);
              return;
            }
            if (isReadyRef.current === true) {
              clearInterval(poller);
              proceedWithFill();
            }
          }, 100);
        }
      });
    };

    playAnimation();

    return () => {
      cancelled = true;
      clearInterval(poller);
    };
  }, []);

  // Parse size to a number if it is a string without units, or keep as string if it has units (like '100%')
  // For SVG viewBox, it's safer to use the size as a dimension
  const dimension = typeof size === 'string' && !isNaN(Number(size)) ? Number(size) : size;
  const dimensionValue = dimension as any; // Cast as any because strings can be '100%' etc but Animated.View is strict

  return (
    <View style={[styles.wrapper, { width: dimensionValue, height: dimensionValue }, style]}>
      <Animated.View style={{ opacity: masterOpacity, width: dimensionValue, height: dimensionValue }}>
        <Svg width={dimension} height={dimension} viewBox="0 0 1195 1195">
          <G transform="translate(0,1195) scale(0.1,-0.1)">
            {PATHS.map((d, i) => {
              // strokeDashoffset: DASH_LENGTH when hidden → 0 when drawn
              const dashOffset = drawAnims[i].interpolate({
                inputRange: [0, 1],
                outputRange: [0, DASH_LENGTHS[i]],
              });
              // Once fillAnim fires, show fill and hide stroke
              const fillOpacity = fillAnim;
              const strokeOpacity = fillAnim.interpolate({
                inputRange: [0, 1],
                outputRange: [1, 0],
              });

              return (
                <AnimatedPath
                  key={i}
                  d={d}
                  // Fill
                  fill={color}
                  fillOpacity={fillOpacity}
                  // Outline draw
                  stroke={color}
                  strokeWidth={24}
                  strokeLinecap="round"
                  strokeLinejoin="round"
                  strokeDasharray={`${DASH_LENGTHS[i]} ${DASH_LENGTHS[i]}`}
                  strokeDashoffset={dashOffset}
                  strokeOpacity={strokeOpacity}
                />
              );
            })}
          </G>
        </Svg>
      </Animated.View>
    </View>
  );
}

const styles = StyleSheet.create({
  wrapper: {
    alignItems: 'center',
    justifyContent: 'center',
  },
});
